print(0.1 + 0.1,
      0.2 + 0.2,
      2 * 0.1,
      2 * 0.2,
       0.2 + 0.1,
       3 * 0.1)

#Integers and floating point numbers
print(4/2,
      1 + 2.0,
      2 * 3.0,
      3.0 ** 2)

